    //Nếu nhỏ hơn 18 tuổi trả về false
    public static boolean checkDate(Date birthDate, String message) {
        System.out.println(birthDate);
        Calendar currentDate = Calendar.getInstance();

        Calendar birthCalendar = Calendar.getInstance();
        birthCalendar.setTime(birthDate);
        System.out.println(birthCalendar);
        int age = currentDate.get(Calendar.YEAR) - birthCalendar.get(Calendar.YEAR);
        System.out.println(age);
        if (currentDate.get(Calendar.DAY_OF_YEAR) < birthCalendar.get(Calendar.DAY_OF_YEAR)) {
            age--;
        }

        if (age < 18) {
            JOptionPane.showMessageDialog(null, message);
            return false;
        }
        return true;

    }

    //Nếu null trả về true
    public static <T> boolean checkNull(T item) {
        return item == null;
    }

    
    //Nếu đúng regex thì trả về true
    private static boolean checkPattern(String input, Pattern pattern, String message) {
        Matcher matcher = pattern.matcher(input);
        if (matcher.matches()) {
            return true;
        } else {
            JOptionPane.showMessageDialog(null, message);
            return false;
        }
    }

